/** Full string table for locale (EN base + patch). */
export function localeTable(locale: DemoLocale): Record<DemoMsgKey, string> {
  const base = { ...en } as unknown as Record<DemoMsgKey, string>;
  if (locale === "en") return base;
  if (locale === "fr") return { ...base, ...FR };
  return { ...base, ...PT_PATCH };
}

export function demoInterp(
  template: string,
  vars: Record<string, string | number>,
): string {
  return template.replace(/\{\{(\w+)\}\}/g, (_, k) => String(vars[k] ?? ""));
}

export function demoT(
  locale: DemoLocale,
  key: DemoMsgKey,
  vars?: Record<string, string | number>,
): string {
  const table = localeTable(locale);
  let s = table[key] ?? "";
  if (!s) s = String(key);
  return vars ? demoInterp(s, vars) : s;
}